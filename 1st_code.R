calc_length<- function(x){
  count<<-0
  y<<-0
  for(i in 1:length(x)){
    if(x[i]==1){
      count<-count+1
    }else{
      if(count>=1){
        y[i]<<-count
        count<-0
      }else count<-0
    }
  }
  y<-na.omit(y)
  y<-y[-1]
  return (mean(y))
}

